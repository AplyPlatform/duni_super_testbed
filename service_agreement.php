           				② 파트너는 서비스이용금액 등에 대해 계약서에 기재된 금액을 초과하여 청구하지 아니합니다. 다만, 이용자의 책임 있는 사유로 서비스이용금액 등의 산정에 관련된 사항이 변경됨으로 인해 계약서에 기재된 금액을 초과하게 되는 경우에는, 그 변경 시에 초과금액을 미리 이용자에게 고지한 경우에 한해 초과된 금액을 청구할 수 있습니다.<br />
           				③ 파트너는 제1항과 제2항의 규정에 의한 금액 이외에 수고비등 어떠한 명목의 금액도 추가로 청구하지 아니합니다.
