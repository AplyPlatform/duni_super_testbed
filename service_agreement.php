           				3. 회사의 승낙의 의사표시에는 이용자의 구매 신청에 대한 확인 및 판매가능 여부, 구매신청의 정정 취소등에 관한 정보등을 포함하여야 합니다.
           				</p>
           				
           				<h6>제11조(지급방법)</h6>
           				<p>사이트에서 구매한 재화 또는 용역에 대한 대금지급방법은 다음 각호의 방법중 가용한 방법으로 할 수 있습니다. 단, 회사는 이용자의 지급방법에 대하여 재화 등의 대금에 어떠한 명목의 수수료도 추가하여 징수할 수 없습니다.<br />
           				① 폰뱅킹, 인터넷뱅킹, 메일 뱅킹 등의 각종 계좌이체<br />
           				② 선불카드, 직불카드, 신용카드 등의 각종 카드 결제<br />
           				③ 온라인무통장입금<br />
           				④ 전자화폐에 의한 결제<br />
           				⑤ 수령시 대금지급<br />
           				⑥ 마일리지 등 회사가 지급한 포인트에 의한 결제<br />
           				⑦ 회사와 계약을 맺었거나 회사가 인정한 상품권에 의한 결제<br />
           				⑧ 기타 전자적 지급 방법에 의한 대금 지급 등
           				</p>
           				
           				<h6>제12조(수신확인통지, 구매신청 변경 및 취소)</h6>
           				<p>
           				① 회사는 이용자의 구매신청이 있는 경우 이용자에게 수신확인통지를 합니다.<br />
           				② 수신확인통지를 받은 이용자는 의사표시의 불일치등이 있는 경우에는 수신확인통지를 받은 후 즉시 구매신청 변경 및 취소를 요청할 수 있고 회사는 배송전에 이용자의 요청이 있는 경우에는 지체없이 그 요청에 따라 처리하여야 합니다. 다만 이미 대금을 지불한 경우에는 제15조의 청약철회 등에 관한 규정에 따릅니다.
           				</p>
           				
           				<h6>제13조(개인정보보호)</h6>
           				<p>
           				1. 회사는 이용자의 정보수집시 구매계약 이행에 필요한 최소한의 정보를 수집합니다. 다음 사항을 필수사항으로 하며 그 외 사항은 선택사항으로 합니다.<br />
           				① 성명<br />
           				② 주소<br />
           				③ 전화번호<br />
           				④ 희망ID(회원의 경우)<br />
           				⑤ 비밀번호(회원의 경우)<br />
           				⑥ 전자우편주소(또는 이동전화번호)<br />
           				2. 회사가 이용자의 개인식별이 가능한 개인정보를 수집하는 때에는 반드시 당해 이용자의 동의를 받습니다.<br />
           				3. 제공된 개인정보는 당해 이용자의 동의없이 목적외의 이용이나 제3자에게 제공할 수 없으며, 이에 대한 모든 책임은 회사가 집니다. 다만, 다음의 경우에는 예외로 합니다.<br />
           				① 서비스이용상 파트너업체(개인/사업자)에게 서비스이용에 필요한 최소한의 이용자의 정보(성명, 주소, 전화번호)를 알려주는 경우<br />
           				② 통계작성, 학술연구 또는 시장조사를 위하여 필요한 경우로서 특정 개인을 식별할 수 없는 형태로 제공하는 경우<br />
           				③ 재화등의 거래에 따른 대금정산을 위하여 필요한 경우<br />
           				④ 도용방지를 위하여 본인확인에 필요한 경우<br />
           				⑤  법률의 규정 또는 법률에 의하여 필요한 불가피한 사유가 있는 경우<br />
           				4. 회사가 제2항과 제3항에 의해 이용자의 동의를 받아야 하는 경우에는 개인정보관리 책임자의 신원(소속, 성명 및 전화번호, 기타 연락처), 정보의 수집목적 및 이용목적, 제3자에 대한 정보제공 관련사항(제공받은자, 제공목적 및 제공할 정보의 내용) 등 정보통신망이용촉진등에관한법률 제22조제2항이 규정한 사항을 미리 명시하거나 고지해야 하며 이용자는 언제든지 이 동의를 철회할 수 있습니다.<br />
           				5. 이용자는 언제든지 회사가 가지고 있는 자신의 개인정보에 대해 열람 및 오류정정을 요구할 수 있으며 회사는 이에 대해 지체없이 필요한 조치를 취할 의무를 집니다. 이용자가 오류의 정정을 요구한 경우에는 회사는 그 오류를 정정할 때까지 당해 개인정보를 이용하지 않습니다.<br />
           				6. 회사는 개인정보 보호를 위하여 관리자를 한정하여 그 수를 최소화하며 신용카드, 은행계좌 등을 포함한 이용자의 개인정보의 분실, 도난, 유출, 변조 등으로 인한 이용자의 손해에 대하여 모든 책임을 집니다.<br />
           				7. 회사 또는 그로부터 개인정보를 제공받은 제3자는 개인정보의 수집목적 또는 제공받은 목적을 달성한 때에는 당해 개인정보를 지체없이 파기합니다.
           				</p>
           				
