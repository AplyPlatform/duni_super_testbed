           				<h6>제22조(계약)</h6>
           				<p>
           				1. 파트너는 이용자로부터 드론 활용 서비스를 의뢰 받는 때에는 다음 각 호의 규정을 이용자가 이해할 수 있도록 설명하고, 이 약관을 이용자에게 명시합니다. 이 경우 이용자의 요구가 있을 시에는 이 약관의 사본을 교부합니다. 다만, 계약서의 이 약관의 전부가 기재된 경우에는 계약서의 교부로 약관사본의 교부에 갈음합니다.<br />
           				① 이 약관 제25조, 제26조, 제28조, 제29조, 제32조, 제33조, 제34조 및 제37조의 규정 내용.<br />
           				② 이용자가 피해를 당하였을 경우 피해 구제방법 및 관련기관의 명칭과 전화번호<br />
