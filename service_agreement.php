           				② 회원은 자신의 ID 및 비밀번호를 제3자에게 이용하게 해서는 안됩니다.<br />
           				③ 회원이 자신의 ID 및 비밀번호를 도난당하거나 제3자가 사용하고 있음을 인지한 경우에는 바로 “몰”에 통보하고 회사의 안내가 있는 경우에는 그에 따라야 합니다.
           				</p>
           				
           				<h6>제16조(이용자의 의무)</h6>
           				<p>
           				1. 이용자는 다음 행위를 하여서는 안됩니다.<br />
           				① 신청 또는 변경시 허위 내용의 등록<br />
           				② 타인의 정보 도용<br />
           				③ 사이트에 게시된 정보의 변경<br />
           				④ 회사가 정한 정보 이외의 정보(컴퓨터 프로그램 등) 등의 송신 또는 게시<br />
           				⑤ 회사·기타 제3자의 저작권 등 지적재산권에 대한 침해<br />
           				⑥ 회사·기타 제3자의 명예를 손상시키거나 업무를 방해하는 행위<br />
           				⑦ 외설 또는 폭력적인 메시지, 화상, 음성, 기타 공서양속에 반하는 정보를 사이트에 공개 또는 게시하는 행위
           				</p>
           				
           				<h6>제17조(연결 '서비스'와 '피연결서비스' 간의 관계)</h6>
           				<p>
           				① 상위 서비스와 하위 서비스가 하이퍼 링크(예: 하이퍼 링크의 대상에는 문자, 그림 및 동화상 등이 포함됨)방식 등으로 연결된 경우, 전자를 연결 서비스(웹 사이트)이라고 하고 후자를 피연결서비스(웹사이트)이라고 합니다.<br />
           				② 연결서비스는 피연결서비스가 독자적으로 제공하는 재화 등에 의하여 이용자와 행하는 거래에 대해서 보증책임을 지지 않는다는 뜻을 연결서비스의 초기화면 또는 연결되는 시점의 팝업화면으로 명시한 경우에는 그 거래에 대한 보증책임을 지지 않습니다.
           				</p>
           				
           				<h6>제18조(저작권의 귀속 및 이용제한)</h6>
           				<p>
           				1. 회사가 작성한 저작물에 대한 저작권 기타 지적재산권은 회사에 귀속합니다.<br />
           				2. 이용자는 서비스를 이용함으로써 얻은 정보 중 회사에 지적재산권이 귀속된 정보를 회사의 사전 승낙없이 복제, 송신, 출판, 배포, 방송 기타 방법에 의하여 영리목적으로 이용하거나 제3자에게 이용하게 하여서는 안됩니다.<br />
           				3. 회사는 약정에 따라 이용자에게 귀속된 저작권을 사용하는 경우 당해 이용자에게 통보하여야 합니다.<br />
           				4. 이용자가 작성한 저작물에 대한 모든 권리 및 책임은 이를 작성한 이용자에게 있으며, 회사는 이용자의 사전 동의 하에 2차 저작물 또는 편집저작물을 창작하여 이용할 수 있습니다. 단, 이렇게 창작된 2차적저작물 또는 편집저작물은 ㈜어플라이 이외의 제 3자의 무단사용으로부터 보호됩니다. 또한 이용자가 게시하거나 등록하는 저작물의 내용이 다음 각 항에 해당한다고 판단되는 경우에 관련법에 의거하여 해당 저작물을 임시조치를 취하거나 사전통지 없이 삭제할 수 있습니다.<br />
           				① 다른 이용자 또는 제 3자를 비방하거나 명예를 손상시키는 내용인 경우<br />
           				② 회사의 저작권 및 제 3자의 저작권 등 타인의 권리를 침해하는 내용인 경우<br />
           				③ 타인의 명의를 도용한 경우<br />
           				④ 개인간의 금전거래를 요구하는 경우<br />
           				⑤ 회사가 서비스의 성격 및 게시물의 위치에 부합되지 않는다고 판단하는 경우<br />
           				⑥ 기타 관계법령에 위배된다고 판단되는 경우
           				</p>
           				
           				<h6>제19조(분쟁해결)</h6>
           				<p>
           				① 회사는 이용자가 제기하는 정당한 의견이나 불만을 반영하고 그 피해를 보상처리하기 위하여 피해보상처리기구를 설치, 운영합니다.<br />
           				② 회사는 이용자로부터 제출되는 불만사항 및 의견은 우선적으로 그 사항을 처리합니다. 다만, 신속한 처리가 곤란한 경우에는 이용자에게 그 사유와 처리일정을 즉시 통보해드립니다.<br />
           				③ 회사와 이용자 간에 발생한 전자상거래 분쟁과 관련하여 이용자의 피해구제신청이 있는 경우에는 공정거래위원회 또는 시·도지사가 의뢰하는 분쟁조정기관의 조정에 따를 수 있습니다.
           				</p>
           				
           				<h6>제20조(재판권 및 준거법)</h6>
           				<p>
           				① 회사와 이용자 간에 발생한 전자상거래 분쟁에 관한 소송은 제소 당시의 이용자의 주소에 의하고, 주소가 없는 경우에는 거소를 관할하는 지방법원의 전속관할로 합니다. 다만, 제소 당시 이용자의 주소 또는 거소가 분명하지 않거나 외국 거주자의 경우에는 민사소송법상의 관할법원에 제기합니다.<br />
           				② 회사와 이용자간에 제기된 전자상거래 소송에는 한국법을 적용합니다.
           				</p>
           				
